    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterActionPerformed
        
        JOptionPane d = new JOptionPane();
        
        if ( this.checkForm() ) {
            
            for ( Participant participant : this.getJListParticipant() ) {
                
                Invitation invitation = new Invitation();
                invitation.setEvenement( this.getJComboBoxEvenement() );
                invitation.setParticipant( participant );
                
                new InvitationDAO().create( invitation );
            }

            d.showMessageDialog(this, "Votre invitation a bien été ajouté !", "Message", JOptionPane.INFORMATION_MESSAGE);
            
            this.setVisible( false );

            new AccueilWD().setVisible( true );
        
        } else {
            
            d.showMessageDialog(this, "Veuillez vérifier les informations", "Message", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAjouterActionPerformed

    private void jComboBoxEvenementItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxEvenementItemStateChanged
        
        if ( evt.getStateChange() == ItemEvent.SELECTED ) {
        
            this.participants = new ParticipantDAO().findAllNotByEvenement( this.getJComboBoxEvenement().getId() );
            
            DefaultListModel listModel = new DefaultListModel();
        
            for ( Participant participant : this.participants )
                listModel.addElement( participant.getNom() + participant.getPrenom() + " - " + participant.getEmail() );

            this.jListParticipant.setSelectionMode( ListSelectionModel.MULTIPLE_INTERVAL_SELECTION );
            this.jListParticipant.setLayoutOrientation( JList.VERTICAL );
            this.jListParticipant.setVisibleRowCount( -1 );
            this.jListParticipant.setModel( listModel );
        }
    }//GEN-LAST:event_jComboBoxEvenementItemStateChanged

