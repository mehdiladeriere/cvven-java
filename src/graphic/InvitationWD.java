    public void jComboBoxEvenement() {
        
        this.jComboBoxEvenement.removeAllItems();
        
        // Add Data
        for ( Evenement evenement : this.evenements )
            this.jComboBoxEvenement.addItem( evenement.getIntitule() );
    }
    
    public void jListParticipant() {
        
        DefaultListModel listModel = new DefaultListModel();
        
        // Add Data
        for ( Participant participant : this.participants )
            listModel.addElement( participant.getNom() + " " + participant.getPrenom() + " - " + participant.getEmail() );
    
        this.jListParticipant.setSelectionMode( ListSelectionModel.MULTIPLE_INTERVAL_SELECTION );
        this.jListParticipant.setLayoutOrientation( JList.VERTICAL );
        this.jListParticipant.setVisibleRowCount( -1 );
        this.jListParticipant.setModel( listModel );
    }
    
    // Get Component content
    
    public Evenement getJComboBoxEvenement() {
        
        if ( this.jComboBoxEvenement.getSelectedIndex() != -1 )
            return this.evenements.get( this.jComboBoxEvenement.getSelectedIndex() );
        
        return null;
    }
    
    public ArrayList <Participant> getJListParticipant() {
        
        ArrayList <Participant> list = new ArrayList <Participant> ();
        
        for ( int indice : this.jListParticipant.getSelectedIndices() )
            list.add( this.participants.get( indice ) );
        
        return list;
    }
    
    // Check Component content
    
    public boolean checkJTextField() {
        
        for ( JTextField field : this.jTextField ) {
            if ( field.getText().length() == 0 )
                return false;
        }
            
        return true;
    }
    
    public boolean checkJTextArea() {
        
        for ( JTextArea field : this.jTextArea ) {
            if ( field.getText().length() == 0 )
                return false;
        }
        
        return true;
    }
    
    public boolean checkJComboBox() {
        
        for ( JComboBox field : this.jComboBox ) {
            if ( field.getSelectedIndex() == -1 )
                return false;
        }
        
        return true;
    }
    
    public boolean checkJList() {
        
        for ( JList field : this.jList ) {
            if ( field.getSelectedIndices().length == 0 )
                return false;
        }
        
        return true;
    }
    
    public boolean checkJDateChooser() {
        
        for ( JDateChooser field : this.jDateChooser ) {
            if ( field.getDate() == null )
                return false;
        }
        
        return true;
    }
    
    // Check Form
    
    public boolean checkForm() {
        
        return checkJTextField() && checkJTextArea() && checkJComboBox() && checkJList() && checkJDateChooser();
    }

